int		ft_wordcount(char const *s)
{
	int		words;
	
	words = 0;
	while (*s++)
	{
		if ((*s > 32 && *s < 127)  && (*(s + 1) == ' ' || *(s + 1) == '\t' || *(s + 1) == '\0'))
			words++;
	}
	return (words);
}
